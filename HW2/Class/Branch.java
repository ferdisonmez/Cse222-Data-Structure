public class Branch {
    private String name;
    public Branch(String name) {
        this.name = name;
    }

    /***
     * Getter
     * @return name
     */
    public String getName() {
        return name;
    }

    /***
     * Setter
     * @param name is string
     */
    public void setName(String name) {
        this.name = name;
    }


}
